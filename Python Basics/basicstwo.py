#Arithmetic Operations
# Adding
print(10 + 3)
# Subtracting
print(10 - 3)
# Multiplication
print(10 * 3)
# Division One Way (Floating point number)
print(10 / 3)
# Division The Other way (An Integer)
print(10 // 3)
# Module list (Returns remainder of the division)
print(10 % 3)
# Exponent (Which is the power)
print(10 ** 3)